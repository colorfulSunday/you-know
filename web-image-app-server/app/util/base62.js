const TIMES = 62;
module.exports = {
  encodeBase62(value) {
    let num = value;
    let result1 = '';
    while (num >= 1) {
      result1 += base62Arr[value % TIMES];
      num = num / TIMES;
    }
    return result1.split('').reverse().join('');
  },
};
